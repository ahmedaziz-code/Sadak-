    /**
     * @param $gov
     * @param $ville
     * @return Utilisateur[]
     */
    public function findAllByAdressAndType($gov, $ville): array
    {
        return $this->createQueryBuilder('u')
            ->where('u.typeU LIKE :type')
            ->andWhere('u.gouvernorat LIKE :gov')
            ->andWhere('u.ville LIKE :ville')
            ->setParameters(['type' => 'volontaire', 'gov'=>$gov, 'ville'=>$ville])
            ->getQuery()
            ->getResult();
    }

